create or replace function
  public.review_professional_credential_submission(
    p_submission_id uuid,
    p_new_status text,
    p_review_note text
  )
returns void
language plpgsql
security definer
set search_path = ''
as $$
declare
  current_user_id uuid := auth.uid();
  submission_owner_id uuid;
  old_status text;
  derived_profile_status text;
  normalized_note text :=
    nullif(left(trim(coalesce(p_review_note, '')), 2000), '');
begin
  if current_user_id is null
     or not private.is_admin() then
    raise exception 'Active Admin access is required.'
      using errcode = '42501';
  end if;

  if p_new_status not in (
    'submitted',
    'under_review',
    'needs_changes',
    'verified',
    'rejected',
    'revoked'
  ) then
    raise exception 'Invalid review status.'
      using errcode = '22023';
  end if;

  select
    submission.owner_user_id,
    submission.submission_status
  into
    submission_owner_id,
    old_status
  from public.professional_credential_submissions submission
  where submission.id = p_submission_id
  for update;

  if submission_owner_id is null then
    raise exception 'Credential submission was not found.'
      using errcode = 'P0002';
  end if;

  if submission_owner_id = current_user_id then
    raise exception
      'Another Admin must review this credential submission.'
      using errcode = '42501';
  end if;

  update public.professional_credential_submissions
  set
    submission_status = p_new_status,
    review_note = normalized_note,
    reviewed_at = now(),
    reviewed_by = current_user_id,
    updated_at = now()
  where id = p_submission_id;

  insert into public.professional_credential_review_events (
    submission_id,
    owner_user_id,
    actor_user_id,
    previous_status,
    new_status,
    review_note
  )
  values (
    p_submission_id,
    submission_owner_id,
    current_user_id,
    old_status,
    p_new_status,
    normalized_note
  );

  select
    case
      when bool_or(submission_status = 'verified')
        then 'verified'
      when bool_or(submission_status = 'under_review')
        then 'under_review'
      when bool_or(submission_status = 'submitted')
        then 'submitted'
      when bool_or(submission_status = 'needs_changes')
        then 'needs_changes'
      when bool_or(submission_status = 'rejected')
        then 'rejected'
      when bool_or(submission_status = 'revoked')
        then 'revoked'
      else 'not_submitted'
    end
  into derived_profile_status
  from public.professional_credential_submissions
  where owner_user_id = submission_owner_id;

  update public.profiles
  set
    professional_verification_status =
      coalesce(derived_profile_status, 'not_submitted'),
    professional_verification_review_note = normalized_note,
    professional_verification_reviewed_at = now(),
    professional_verification_reviewed_by = current_user_id
  where id = submission_owner_id;
end;
$$;

revoke all
