create or replace function
  public.submit_professional_credential_submission(
    p_id uuid,
    p_credential_type text,
    p_document_title text,
    p_issuing_authority text,
    p_license_number text,
    p_country text,
    p_issue_date date,
    p_expiry_date date,
    p_document_path text,
    p_original_filename text,
    p_mime_type text,
    p_file_size_bytes bigint
  )
returns uuid
language plpgsql
security definer
set search_path = ''
as $$
declare
  current_user_id uuid := auth.uid();
  normalized_type text := nullif(trim(p_credential_type), '');
  normalized_title text := nullif(trim(p_document_title), '');
begin
  if current_user_id is null then
    raise exception 'Authentication is required.'
      using errcode = '42501';
  end if;

  if p_id is null then
    raise exception 'Submission identifier is required.'
      using errcode = '22023';
  end if;

  if normalized_type is null
     or char_length(normalized_type) > 100 then
    raise exception 'A valid credential type is required.'
      using errcode = '22023';
  end if;

  if normalized_title is null
     or char_length(normalized_title) > 200 then
    raise exception 'A valid document title is required.'
      using errcode = '22023';
  end if;

  if split_part(p_document_path, '/', 1)
     <> current_user_id::text then
    raise exception 'Invalid document path.'
      using errcode = '42501';
  end if;

  if p_mime_type not in (
    'application/pdf',
    'image/jpeg',
    'image/png'
  ) then
    raise exception 'Unsupported document type.'
      using errcode = '22023';
  end if;

  if p_file_size_bytes <= 0
     or p_file_size_bytes > 10485760 then
    raise exception 'Document size is invalid.'
      using errcode = '22023';
  end if;

  if p_issue_date is not null
     and p_expiry_date is not null
     and p_expiry_date < p_issue_date then
    raise exception 'Expiry date cannot precede issue date.'
      using errcode = '22023';
  end if;

  if not exists (
    select 1
    from storage.objects object
    where object.bucket_id = 'credential-documents'
      and object.name = p_document_path
  ) then
    raise exception 'Uploaded document was not found.'
      using errcode = 'P0002';
  end if;

  insert into public.professional_credential_submissions (
    id,
    owner_user_id,
    credential_type,
    document_title,
    issuing_authority,
    license_number,
    country,
    issue_date,
    expiry_date,
    document_path,
    original_filename,
    mime_type,
    file_size_bytes,
    submission_status
  )
  values (
    p_id,
    current_user_id,
    normalized_type,
    normalized_title,
    nullif(trim(p_issuing_authority), ''),
    nullif(trim(p_license_number), ''),
    nullif(trim(p_country), ''),
    p_issue_date,
    p_expiry_date,
    p_document_path,
    left(trim(p_original_filename), 255),
    p_mime_type,
    p_file_size_bytes,
    'submitted'
  );

  update public.profiles
  set
    professional_verification_status = 'submitted',
    professional_verification_review_note = null,
    professional_verification_reviewed_at = null,
    professional_verification_reviewed_by = null
  where id = current_user_id;

  return p_id;
end;
$$;

