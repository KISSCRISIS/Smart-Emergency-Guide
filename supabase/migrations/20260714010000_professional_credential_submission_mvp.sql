create or replace function private.protect_profile_security_fields()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
declare
  protected_fields_changed boolean;
  verification_fields_changed boolean;
  allowed_submission_transition boolean;
begin
  if auth.uid() is not null
     and old.phone is not null
     and new.phone is distinct from old.phone then
    raise exception
      'Phone number cannot be changed from an authenticated client'
      using errcode = '42501';
  end if;

  if auth.uid() is not null
     and not private.is_admin() then
    protected_fields_changed := row(
      new.id,
      new.email,
      new.role,
      new.admin_level,
      new.status,
      new.account_status,
      new.subscription_status,
      new.educator_status,
      new.ai_enabled,
      new.reviewed_at,
      new.reviewed_by,
      new.review_note,
      new.created_at
    ) is distinct from row(
      old.id,
      old.email,
      old.role,
      old.admin_level,
      old.status,
      old.account_status,
      old.subscription_status,
      old.educator_status,
      old.ai_enabled,
      old.reviewed_at,
      old.reviewed_by,
      old.review_note,
      old.created_at
    );

    if protected_fields_changed then
      raise exception
        'Protected profile fields cannot be changed by this user'
        using errcode = '42501';
    end if;

    verification_fields_changed := row(
      new.professional_verification_status,
      new.professional_verification_review_note,
      new.professional_verification_reviewed_at,
      new.professional_verification_reviewed_by
    ) is distinct from row(
      old.professional_verification_status,
      old.professional_verification_review_note,
      old.professional_verification_reviewed_at,
      old.professional_verification_reviewed_by
    );

    if verification_fields_changed then
      allowed_submission_transition :=
        new.id = auth.uid()
        and new.professional_verification_status = 'submitted'
        and old.professional_verification_status in (
          'not_submitted',
          'draft',
          'submitted',
          'needs_changes',
          'rejected',
          'expired'
        )
        and new.professional_verification_review_note is null
        and new.professional_verification_reviewed_at is null
        and new.professional_verification_reviewed_by is null
        and exists (
          select 1
          from public.professional_credential_submissions submission
          where submission.owner_user_id = auth.uid()
            and submission.submission_status = 'submitted'
        );

      if not allowed_submission_transition then
        raise exception
          'Professional verification fields cannot be changed directly'
          using errcode = '42501';
      end if;
    end if;
  end if;

  return new;
end;
$$;

revoke all
on function private.protect_profile_security_fields()
from public, anon, authenticated;

